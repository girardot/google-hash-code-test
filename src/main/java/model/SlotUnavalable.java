package model;

public class SlotUnavalable {


    public final int x, y;

    public SlotUnavalable(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "SlotUnavalable{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
